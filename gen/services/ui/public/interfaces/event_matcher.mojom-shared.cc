        // static
        bool EventMatcher_Data::Validate(
            const void* data,
            mojo::internal::ValidationContext* validation_context)
        {
            if (!data)
                return true;

            if (!ValidateStructHeaderAndClaimMemory(data, validation_context))
                return false;

            // NOTE: The memory backing |object| may be smaller than |sizeof(*object)| if
            // the message comes from an older version.
            const EventMatcher_Data* object = static_cast<const EventMatcher_Data*>(data);

            static constexpr struct {
                uint32_t version;
                uint32_t num_bytes;
            } kVersionSizes[] = { { 0, 64 } };

            if (object->header_.version <= kVersionSizes[arraysize(kVersionSizes) - 1].version) {
                // Scan in reverse order to optimize for more recent versions.
                for (int i = arraysize(kVersionSizes) - 1; i >= 0; --i) {
                    if (object->header_.version >= kVersionSizes[i].version) {
                        if (object->header_.num_bytes == kVersionSizes[i].num_bytes)
                            break;

                        ReportValidationError(
                            validation_context,
                            mojo::internal::VALIDATION_ERROR_UNEXPECTED_STRUCT_HEADER);
                        return false;
                    }
                }
            } else if (object->header_.num_bytes < kVersionSizes[arraysize(kVersionSizes) - 1].num_bytes) {
                ReportValidationError(
                    validation_context,
                    mojo::internal::VALIDATION_ERROR_UNEXPECTED_STRUCT_HEADER);
                return false;
            }

            if (!::ui::mojom::internal::AcceleratorPhase_Data ::Validate(object->accelerator_phase, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->type_matcher, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->flags_matcher, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->ignore_flags_matcher, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->key_matcher, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->pointer_kind_matcher, validation_context))
                return false;

            if (!mojo::internal::ValidateStruct(object->pointer_location_matcher, validation_context))
                return false;

            return true;
        }

