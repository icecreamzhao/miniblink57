        // static
        bool GpuMemoryBufferHandle_Data::Validate(
            const void* data,
            mojo::internal::ValidationContext* validation_context)
        {
            if (!data)
                return true;

            if (!ValidateStructHeaderAndClaimMemory(data, validation_context))
                return false;

            // NOTE: The memory backing |object| may be smaller than |sizeof(*object)| if
            // the message comes from an older version.
            const GpuMemoryBufferHandle_Data* object = static_cast<const GpuMemoryBufferHandle_Data*>(data);

            static constexpr struct {
                uint32_t version;
                uint32_t num_bytes;
            } kVersionSizes[] = { { 0, 48 } };

            if (object->header_.version <= kVersionSizes[arraysize(kVersionSizes) - 1].version) {
                // Scan in reverse order to optimize for more recent versions.
                for (int i = arraysize(kVersionSizes) - 1; i >= 0; --i) {
                    if (object->header_.version >= kVersionSizes[i].version) {
                        if (object->header_.num_bytes == kVersionSizes[i].num_bytes)
                            break;

                        ReportValidationError(
                            validation_context,
                            mojo::internal::VALIDATION_ERROR_UNEXPECTED_STRUCT_HEADER);
                        return false;
                    }
                }
            } else if (object->header_.num_bytes < kVersionSizes[arraysize(kVersionSizes) - 1].num_bytes) {
                ReportValidationError(
                    validation_context,
                    mojo::internal::VALIDATION_ERROR_UNEXPECTED_STRUCT_HEADER);
                return false;
            }

            if (!::gfx::mojom::internal::GpuMemoryBufferType_Data ::Validate(object->type, validation_context))
                return false;

            if (!mojo::internal::ValidatePointerNonNullable(
                    object->id, "null id field in GpuMemoryBufferHandle",
                    validation_context)) {
                return false;
            }
            if (!mojo::internal::ValidateStruct(object->id, validation_context))
                return false;

            if (!mojo::internal::ValidateHandleOrInterface(object->shared_memory_handle,
                    validation_context)) {
                return false;
            }

            if (!mojo::internal::ValidateStruct(object->native_pixmap_handle, validation_context))
                return false;

            if (!mojo::internal::ValidateHandleOrInterface(object->mach_port,
                    validation_context)) {
                return false;
            }

            return true;
        }

