template <class _Ty>
constexpr const _Ty& std_max(const _Ty& _Left, const _Ty& _Right)
{
    return _Left < _Right ? _Right : _Left;
}

template <class _Ty>
constexpr const _Ty& std_min(const _Ty& _Left, const _Ty& _Right)
{
    return _Left > _Right ? _Right : _Left;
}

